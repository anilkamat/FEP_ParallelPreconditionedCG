function tet_mesh_display ( prefix, solid )

%*****************************************************************************80
%
%% TET_MESH_DISPLAY displays a tet mesh.
%
%  Discussion:
%
%    The edges of all tetrahedrons will be displayed automatically.
%
%    The input argument SOLID can be used to specify the indices of
%    tetrahedrons that are to be displayed as solids.  Specifying a single
%    nonzero index will highlight the corresponding element.  If SOLID
%    is not supplied as an argument, it will be prompted for, and can
%    be given as a MATLAB expression, such as '[]', '1', '1:2:9',
%    or '[3,7,8]'.
%
%    Note that, for the 10 node case, we assume that the element file lists
%    the node indices in the following order:
%
%      P1 P2 P3 P4 P12 P13 P14 P23 P24 P34
%
%    There are many possible conventions for ordering the nodes, and if
%    this particular convention is not followed, the display of 10 node
%    tetrahedrons will be garbled.
%
%  Usage:
%
%    tet_mesh_display ( 'prefix', solid )
%
%    where
%
%      'prefix' is the common filename prefix:
%        'prefix'_nodes.txt     is the name of the node file, and
%        'prefix'_elements.txt  is the name of the element file.
%
%      solid is an empty list, a single integer, or a vector of
%        indices of tetrahedrons to be displayed as solids.
%
%  Licensing:
%
%    This code is distributed under the GNU LGPL license.
%
%  Modified:
%
%    03 March 2011
%
%  Author:
%
%    John Burkardt
%
%  Input:
%
%    character PREFIX, the common prefix of the node and element files.
%
%    integer SOLID(), a vector of indices of elements to be displayed
%    as solids.  This can be an empty vector, a single index, or a vector.
%
  timestamp ( );
  fprintf ( 1, '\n' );
  fprintf ( 1, 'TET_MESH_DISPLAY:\n' );
  fprintf ( 1, '  MATLAB/Octave version %s\n', version ( ) );
  fprintf ( 1, '  Display a wireframe plot of a tet mesh.\n' );
%
%  First argument is the file prefix.
%
  if ( nargin < 1 )
    fprintf ( 1, '\n' );
    fprintf ( 1, 'TET_MESH_DISPLAY:\n' );
    prefix = input ( ...
      '  Enter the common prefix of the node and element files:  ' );
  end
%
%  Form the file names.
%
  node_filename = strcat ( prefix, '_nodes.txt' );
  element_filename = strcat ( prefix, '_elements.txt' );
%
%  Read the node data.
%
  [ dim_num, node_num ] = r8mat_header_read (  node_filename );

  fprintf ( 1, '\n' );
  fprintf ( 1, '  Read the header of "%s".\n', node_filename );
  fprintf ( 1, '\n' );
  fprintf ( 1, '  Spatial dimension DIM_NUM = %d\n', dim_num );
  fprintf ( 1, '  Number of points NODE_NUM = %d\n', node_num );

  if ( dim_num ~= 3 )
    fprintf ( 1, '\n' );
    fprintf ( 1, 'TET_MESH_DISPLAY - Fatal error!\n' );
    fprintf ( 1, '  Dataset must have spatial dimension 3.\n' );
    error ( 'TET_MESH_DISPLAY - Fatal error!' );
  end

  node_xyz = r8mat_data_read ( node_filename, dim_num, node_num );

  fprintf ( 1, '\n' );
  fprintf ( 1, '  Read the data in "%s".\n', node_filename );

  r8mat_transpose_print_some ( dim_num, node_num, node_xyz, 1, 1, dim_num, 5, ...
    '  First 5 Nodes:' );
%
%  Read the tet data.
%
  [ tet_order, tet_num ] = i4mat_header_read ( element_filename );

  if ( tet_order ~= 4 && tet_order ~= 10)
    fprintf ( 1, '\n' );
    fprintf ( 1, 'TET_MESH_DISPLAY - Fatal error!\n' );
    fprintf ( 1, '  Data is not for a 4 node or 10 node tet mesh.\n' );
    error ( 'TET_MESH_DISPLAY - Fatal error!' );
  end

  fprintf ( 1, '\n' );
  fprintf ( 1, '  Read the header of "%s".\n', element_filename );
  fprintf ( 1, '\n' );
  fprintf ( 1, '  Tetrahedron order = %d\n', tet_order );
  fprintf ( 1, '  Number of tetrahedrons  = %d\n', tet_num );

  tet_node = i4mat_data_read ( element_filename, tet_order, tet_num );

  fprintf ( 1, '\n' );
  fprintf ( 1, '  Read the data in "%s".\n', element_filename );

  i4mat_transpose_print_some ( tet_order, tet_num, ...
    tet_node, 1, 1, tet_order, 5, '  First 5 tetrahedrons:' );
%
%  Detect and correct 0-based indexing.
%
  tet_node = mesh_base_one ( node_num, tet_order, tet_num, tet_node );
%
%  User can enter a MATLAB expression for tetrahedrons to exhibit, such as
%  1
%  2:8
%  [ 3, 7, 18 ]
%
  show = zeros ( tet_num, 1 );

  if ( nargin < 2 )
    solid = input ( '  Enter indices of tets to show as solids:  ' );
  end
  show ( solid ) = 1;
%
%  Make the plot.
%
  size = 40;
  face_color = 'k';

  scatter3 ( node_xyz(1,:), node_xyz(2,:), node_xyz(3,:), size, ...
    face_color, 'filled' )

  face_color = 'b';

  for tet = 1 : tet_num

    if ( show ( tet ) )
      face_color = 'b';
    else
      face_color = 'w';
    end

    if ( tet_order == 4 )

      t = [ tet_node(1,tet), tet_node(2,tet), tet_node(3,tet) ];
      patch ( node_xyz(1,t), node_xyz(2,t), node_xyz(3,t), ...
        face_color, 'FaceAlpha', 0.2, 'EdgeColor', 'r', 'LineWidth', 2.0 );
      t = [ tet_node(1,tet), tet_node(2,tet), tet_node(4,tet) ];
      patch ( node_xyz(1,t), node_xyz(2,t), node_xyz(3,t), ...
        face_color, 'FaceAlpha', 0.2, 'EdgeColor', 'r', 'LineWidth', 2.0 );
      t = [ tet_node(1,tet), tet_node(3,tet), tet_node(4,tet) ];
      patch ( node_xyz(1,t), node_xyz(2,t), node_xyz(3,t), ...
        face_color, 'FaceAlpha', 0.2, 'EdgeColor', 'r', 'LineWidth', 2.0 );
      t = [ tet_node(2,tet), tet_node(3,tet), tet_node(4,tet) ];
      patch ( node_xyz(1,t), node_xyz(2,t), node_xyz(3,t), ...
        face_color, 'FaceAlpha', 0.2, 'EdgeColor', 'r', 'LineWidth', 2.0 );

    elseif ( tet_order == 10 )

      t = [ tet_node(1,tet), tet_node(5,tet), tet_node(2,tet), ...
            tet_node(8,tet), tet_node(3,tet), tet_node(6,tet) ];
      patch ( node_xyz(1,t), node_xyz(2,t), node_xyz(3,t), ...
        face_color, 'FaceAlpha', 0.2, 'EdgeColor', 'r', 'LineWidth', 2.0 );
      t = [ tet_node(1,tet), tet_node(5,tet), tet_node(2,tet), ...
            tet_node(9,tet), tet_node(4,tet), tet_node(7,tet) ];
      patch ( node_xyz(1,t), node_xyz(2,t), node_xyz(3,t), ...
        face_color, 'FaceAlpha', 0.2, 'EdgeColor', 'r', 'LineWidth', 2.0 );
      t = [ tet_node(1,tet), tet_node(6,tet), tet_node(3,tet), ...
            tet_node(10,tet), tet_node(4,tet), tet_node(7,tet) ];
      patch ( node_xyz(1,t), node_xyz(2,t), node_xyz(3,t), ...
        face_color, 'FaceAlpha', 0.2, 'EdgeColor', 'r', 'LineWidth', 2.0 );
      t = [ tet_node(2,tet), tet_node(8,tet), tet_node(3,tet), ...
            tet_node(10,tet), tet_node(4,tet), tet_node(9,tet) ];
      patch ( node_xyz(1,t), node_xyz(2,t), node_xyz(3,t), ...
        face_color, 'FaceAlpha', 0.2, 'EdgeColor', 'r', 'LineWidth', 2.0 );

    end

  end

  axis equal
  xlabel ( '--X axis--' )
  ylabel ( '--Y axis--' )
  zlabel ( '--Z axis--' )
%
%  The TITLE function will interpret underscores in the title.
%  We need to unescape such escape sequences!
%
  title_string = s_escape_tex ( prefix );
  title ( title_string )

  filename = sprintf ( '%s.png', prefix );
  print ( '-dpng', filename );
  fprintf ( 1, '  Graphics saved as "%s"\n', filename );

  pause ( 5 )
%
%  Terminate.
%
  fprintf ( 1, '\n' );
  fprintf ( 1, 'TET_MESH_DISPLAY:\n' );
  fprintf ( 1, '  Normal end of execution.\n' );
  fprintf ( 1, '\n' );
  timestamp ( );

  return
end
function column_num = file_column_count ( input_file_name )

%*****************************************************************************80
%
%% FILE_COLUMN_COUNT counts the columns in the first line of a file.
%
%  Discussion:
%
%    The file is assumed to be a simple text file.
%
%    Most lines of the file are presumed to consist of COLUMN_NUM words,
%    separated by spaces.  There may also be some blank lines, and some
%    comment lines, which have a "#" in column 1.
%
%    The routine tries to find the first non-comment non-blank line and
%    counts the number of words in that line.
%
%    If all lines are blanks or comments, it goes back and tries to analyze
%    a comment line.
%
%  Licensing:
%
%    This code is distributed under the GNU LGPL license.
%
%  Modified:
%
%    21 February 2004
%
%  Author:
%
%    John Burkardt
%
%  Input:
%
%    string INPUT_FILE_NAME, the name of the file.
%
%  Output:
%
%    integer COLUMN_NUM, the number of columns in the file.
%
  FALSE = 0;
  TRUE = 1;
%
%  Open the file.
%
  input_unit = fopen ( input_file_name );

  if ( input_unit < 0 )
    fprintf ( 1, '\n' );
    fprintf ( 1, 'FILE_COLUMN_COUNT - Error!\n' );
    fprintf ( 1, '  Could not open the file "%s".\n', input_file_name );
    error ( 'FILE_COLUMN_COUNT - Error!' );
  end
%
%  Read one line, but skip blank lines and comment lines.
%  Use FGETL so we drop the newline character!
%
  got_one = FALSE;

  while ( true )

    line = fgetl ( input_unit );

    if ( line == -1 )
      break;
    end

    if ( s_len_trim ( line ) == 0 )

    elseif ( line(1) == '#' )

    else
      got_one = TRUE;
      break;
    end

  end

  fclose ( input_unit );

  if ( got_one == FALSE )
    fprintf ( 1, '\n' );
    fprintf ( 1, 'FILE_COLUMN_COUNT - Warning!\n' );
    fprintf ( 1, '  The file does not seem to contain any data.\n' );
    column_num = -1;
    return;
  end

  column_num = s_word_count ( line );

  return
end
function row_num = file_row_count ( input_file_name )

%*****************************************************************************80
%
%% FILE_ROW_COUNT counts the number of row records in a file.
%
%  Discussion:
%
%    Each input line is a "RECORD".
%
%    The records are divided into three groups:
%
%    * BLANK LINES (nothing but blanks)
%    * COMMENT LINES (begin with a '#')
%    * DATA RECORDS (anything else)
%
%    The value returned by the function is the number of data records.
%
%    By the way, if the MATLAB routine FGETS is used, instead of
%    FGETL, then the variable LINE will include line termination
%    characters, which means that a blank line would not actually
%    have zero characters.
%
%  Licensing:
%
%    This code is distributed under the GNU LGPL license.
%
%  Modified:
%
%    31 December 2006
%
%  Author:
%
%    John Burkardt
%
%  Input:
%
%    string INPUT_FILE_NAME, the name of the input file.
%
%  Output:
%
%    integer ROW_NUM, the number of rows found.
%
  input_unit = fopen ( input_file_name );

  if ( input_unit < 0 )
    fprintf ( 1, '\n' );
    fprintf ( 1, 'FILE_ROW_COUNT - Error!\n' );
    fprintf ( 1, '  Could not open the file "%s".\n', input_file_name );
    error ( 'FILE_ROW_COUNT - Error!' );
  end

  blank_num = 0;
  comment_num = 0;
  row_num = 0;

  record_num = 0;

  while ( true )

    line = fgetl ( input_unit );

    if ( line == -1 )
      break;
    end

    record_num = record_num + 1;
    record_length = s_len_trim ( line );

    if ( record_length <= 0 )
      blank_num = blank_num + 1;
    elseif ( line(1) == '#' )
      comment_num = comment_num + 1;
    else
      row_num = row_num + 1;
    end

  end

  fclose ( input_unit );

  return
end
function table = i4mat_data_read ( input_filename, m, n )

%*****************************************************************************80
%
%% I4MAT_DATA_READ reads data from an I4MAT file.
%
%  Licensing:
%
%    This code is distributed under the GNU LGPL license.
%
%  Modified:
%
%    27 January 2006
%
%  Author:
%
%    John Burkardt
%
%  Input:
%
%    string INPUT_FILENAME, the name of the input file.
%
%    integer M, N, the number of rows and columns in the data.
%
%  Output:
%
%    integer TABLE(M,N), the point coordinates.
%
  table = zeros ( m, n );
%
%  Build up the format string for reading M real numbers.
%
  string = ' ';

  for i = 0 : m
    string = strcat ( string, ' %d' );
  end

  input_unit = fopen ( input_filename );

  if ( input_unit < 0 )
    fprintf ( 1, '\n' );
    fprintf ( 1, 'I4MAT_DATA_READ - Error!\n' );
    fprintf ( 1, '  Could not open the input file.\n' );
    error ( 'I4MAT_DATA_READ - Error!' );
  end

  i = 0;

  while ( i < n )

    line = fgets ( input_unit );

    if ( line == -1 )
      fprintf ( 1, '\n' );
      fprintf ( 1, 'I4MAT_DATA_READ - Error!\n' );
      fprintf ( 1, '  End of input while reading data.\n' );
      error ( 'I4MAT_DATA_READ - Error!' );
    end

    if ( line(1) == '#' )

    elseif ( s_len_trim ( line ) == 0 )

    else

      [ x, count ] = sscanf ( line, string );

      if ( count == m )
        i = i + 1;
        table(1:m,i) = x(1:m);
      end

    end

  end

  fclose ( input_unit );

  return
end
function [ m, n ] = i4mat_header_read ( input_filename )

